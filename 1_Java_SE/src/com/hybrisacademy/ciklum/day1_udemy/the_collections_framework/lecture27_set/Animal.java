package com.hybrisacademy.ciklum.day1_udemy.the_collections_framework.lecture27_set;

public class Animal {
	String name;
	int age;
	
	public Animal(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	@Override
	public String toString() {
		return "Animal [name=" + name + ", age=" + age + "]";
	}
	
}
